#include <bits/stdc++.h>
using namespace std;

int main() {
    int a = 10, b = 3;

    cout << "a + b = " << a + b << endl;  // Addition
    cout << "a - b = " << a - b << endl;  // Subtraction
    cout << "a * b = " << a * b << endl;  // Multiplication
    cout << "a / b = " << a / b << endl;  // Division
    cout << "a % b = " << a % b << endl;  // Modulus- Returns the division remainder
}

// a + b = 13
// a - b = 7
// a * b = 30
// a / b = 3
// a % b = 1