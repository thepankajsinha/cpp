#include <bits/stdc++.h>
using namespace std;

int main(){
    //find maximum number
    cout << max(5, 10) <<endl; //10

    //find minimum number
    cout << min(5, 10) <<endl; //5

    //find square root
    cout << sqrt(64) <<endl; //8

    //round the number
    cout << round(2.6) <<endl; //3

    //find log value
    cout << log(2); //0.693147
}